#include<iostream>
using namespace std;

int main(){

    int a =5;                         /* int stores 4 bytes */
    cout << a << endl;

    char c = 'a';           /* char stores 1 byte*/
                            /* char value to be always put in single inverted commas */
    cout << c << endl;


    float f = 1.5;            /* float stores 4 bytes */
    cout << f << endl;

    double d = 35.332;        /* double stores 8 bytes */
    cout << d << endl;

    bool b = true;            /* stores 1 byte*/
    cout << b << endl;          /* prints 1 for true and 0 for false*/
}